/// 自動生成サブノードの中間表現（DD-LGX-003 §2.1）。
/// 抽出段階でのみ使用し、最終的に `Node` へ変換される。
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AutoSubnode {
    /// `"{parent_id}#{hex16}"` 形式（SUBNODE-INV-5/6）。
    pub id: String,
    /// 親ドキュメントノードの NodeId。
    pub parent_id: String,
    /// 正規化前の生見出しテキスト（# マーカー・closing # 除去済、前後 trim 済）。
    pub anchor: String,
    /// 正規化済み見出し階層パス（h2: 1 要素、h3: [h2, h3] の 2 要素）。
    pub heading_path: Vec<String>,
    /// 本文のバイト半開区間 `[body_start, end_byte)`。
    pub content_range: (usize, usize),
}

/// サブノード種別（DD-LGX-003 §2.2、v3 model.rs 踏襲）。
