#[derive(Debug, Deserialize, Default)]
struct RawGraphFile {
    #[serde(default)]
    nodes: Vec<RawNode>,
    #[serde(default)]
    edges: Vec<RawEdge>,
}

#[derive(Debug, Deserialize)]
struct RawNode {
    id: String,
    #[serde(rename = "type")]
    type_code: String,
    path: String,
    #[serde(default)]
    parent: Option<String>,
    /// 明示サブノード（`#s:`）の見出しアンカー（例 `## 状態遷移`）。本文区画の解決に使う（LGX-EXT-001 §4.1）。
    #[serde(default)]
    anchor: Option<String>,
    /// Phase 2 Block D: ドキュメント別の見出しレベル指定（既定 [2, 3]）。
    #[serde(default)]
    heading_levels: Option<Vec<u8>>,
}

#[derive(Debug, Deserialize)]
struct RawEdge {
    from: String,
    to: String,
    #[serde(default)]
    kind: Option<String>,
}

/// raw ノードを分類し、サブノードなら親 NodeId を返す（v3 parser.rs:207-239 底本）。
/// 明示サブノード（`#s:`）は parent フィールド必須。自動生成形式は prefix を親に推論。
fn classify_node(raw: &RawNode) -> Result<Option<NodeId>, GraphParseError> {
    let parent_from_field = raw
        .parent
        .as_ref()
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty());

    if let Some((prefix, fragment)) = raw.id.split_once('#') {
        if let Some(name) = fragment.strip_prefix("s:") {
            let _ = name;
            let parent = parent_from_field.ok_or_else(|| {
                GraphParseError::ValidationError(format!(
                    "explicit subnode '{}' requires a `parent` field (SUBNODE-INV-1)",
                    raw.id
                ))
            })?;
            return Ok(Some(parent));
        }
        // 自動生成形式・不明 `#` 形式とも親を推論（明示宣言扱い）。
        let parent = parent_from_field.unwrap_or_else(|| prefix.to_string());
        return Ok(Some(parent));
    }

    if let Some(parent) = parent_from_field {
        return Ok(Some(parent));
    }
    Ok(None)
}

/// raw edge kind 文字列を `EdgeKind` へ変換（v3 parser.rs:241-249 底本）。未知は None。
fn parse_edge_kind(raw: Option<&str>) -> Option<EdgeKind> {
    match raw {
        None => Some(EdgeKind::Chain),
        Some("chain") => Some(EdgeKind::Chain),
        Some("custom") => Some(EdgeKind::Custom),
        Some("parent_child") | Some("parent-child") => Some(EdgeKind::ParentChild),
        _ => None,
    }
}

/// `project_root` 外への `..` 逸脱・絶対パスを拒否（NFR-LGX-001.SEC.06、v3 parser.rs:252-270 底本）。
fn validate_path_scope(path: &str) -> Result<(), GraphParseError> {
    let p = Path::new(path);
    if p.is_absolute() {
        return Err(GraphParseError::ValidationError(format!(
            "absolute path not allowed in graph.toml: {}",
            path
        )));
    }
    for comp in p.components() {
        if matches!(comp, std::path::Component::ParentDir) {
            return Err(GraphParseError::ValidationError(format!(
                "path escapes project_root: {}",
                path
            )));
        }
    }
    Ok(())
}

/// h2/h3 固定で自動サブノードを抽出（DD-LGX-003 §3）。
/// `extract_subnodes_with_levels(parent_id, content, &[2, 3])` の糖衣。
pub fn extract_subnodes(parent_id: &str, content: &str) -> Vec<AutoSubnode> {
    extract_subnodes_with_levels(parent_id, content, &[2, 3])
}

/// 指定レベルの ATX 見出しのみ抽出（DD-LGX-003 §3、M-2 heading_levels 内部属性）。
/// `levels` 空の場合は空 `Vec`（クラッシュなし、REQ.10）。同一入力→同一結果。
pub fn extract_subnodes_with_levels(
    parent_id: &str,
    content: &str,
    levels: &[u8],
) -> Vec<AutoSubnode> {
    let bytes = content.as_bytes();
    let mut pending: Vec<PendingHeading> = Vec::new();

    let mut offset = 0;
    let mut current_h2: Option<String> = None;

    let target = |lv: u8| levels.contains(&lv);

    while offset < bytes.len() {
        let line_end = memchr_newline(bytes, offset).unwrap_or(bytes.len());
        let line = std::str::from_utf8(&bytes[offset..line_end]).unwrap_or("");
        let trimmed_start = line.len() - line.trim_start().len();
        let trimmed = line.trim_start();

        if let Some(level) = atx_level(trimmed) {
            if level == 1 {
                current_h2 = None;
            } else if target(level) {
                let raw = trimmed[(level as usize)..]
                    .trim_start_matches(' ')
                    .trim_end()
                    .trim_end_matches('#')
                    .trim_end();
                let normalized = normalize_heading(raw);
                if normalized.is_empty() {
                    if level == 2 {
                        current_h2 = None;
                    }
                } else {
                    let body_start = next_line_start(bytes, line_end);
                    if level == 2 {
                        current_h2 = Some(normalized.clone());
                    }
                    let h2_context = if level == 3 { current_h2.clone() } else { None };
                    pending.push(PendingHeading {
                        level,
                        raw_line: raw.to_string(),
                        normalized,
                        line_start: offset + trimmed_start,
                        body_start,
                        h2_context,
                    });
                }
            }
        }

        offset = next_line_start(bytes, line_end);
    }

    let mut out: Vec<AutoSubnode> = Vec::with_capacity(pending.len());
    for (idx, ph) in pending.iter().enumerate() {
        let end_byte = pending
            .iter()
            .skip(idx + 1)
            .find(|next| next.level <= ph.level)
            .map(|next| next.line_start)
            .unwrap_or(bytes.len());
        let heading_path = match (ph.level, &ph.h2_context) {
            (3, Some(h2)) => vec![h2.clone(), ph.normalized.clone()],
            _ => vec![ph.normalized.clone()],
        };
        let id = generate_subnode_id(parent_id, &heading_path);
        out.push(AutoSubnode {
            id,
            parent_id: parent_id.to_string(),
            anchor: ph.raw_line.clone(),
            heading_path,
            content_range: (ph.body_start, end_byte),
        });
    }
    out
}

/// 見出し抽出の内部作業型（DD-LGX-003 §2.1、非公開）。
struct PendingHeading {
    level: u8,
    raw_line: String,
    normalized: String,
    line_start: usize,
    body_start: usize,
    h2_context: Option<String>,
}

/// 行頭が `#` × n（n=1..=6）＋空白/タブ/行末のとき ATX 見出しレベルを返す（v3 extractor.rs:117-138 底本）。
fn atx_level(line: &str) -> Option<u8> {
    let mut count = 0u8;
    for b in line.bytes() {
        if b == b'#' {
            count = count.saturating_add(1);
            if count > 6 {
                return None;
            }
        } else {
            break;
        }
    }
    if count == 0 {
        return None;
    }
    let rest = &line.as_bytes()[count as usize..];
    match rest.first() {
        None => Some(count),
        Some(&b' ') | Some(&b'\t') => Some(count),
        _ => None,
    }
}

fn memchr_newline(bytes: &[u8], start: usize) -> Option<usize> {
    bytes[start..]
        .iter()
        .position(|&b| b == b'\n')
        .map(|p| start + p)
}

fn next_line_start(bytes: &[u8], line_end: usize) -> usize {
    if line_end >= bytes.len() {
        return bytes.len();
    }
    line_end + 1
}

/// サブノード ID を生成（DD-LGX-003 §3、SUBNODE-INV-5、v3 id_gen.rs:7-20 実測底本）。
/// `{parent_id}#{SHA-256(parent_id + "|" + heading_path.join("|"))[0..16]}` を小文字 hex で生成。
pub fn generate_subnode_id(parent_id: &str, heading_path: &[String]) -> String {
    let joined = heading_path.join("|");
    let mut hash_input = String::with_capacity(parent_id.len() + 1 + joined.len());
    hash_input.push_str(parent_id);
    hash_input.push('|');
    hash_input.push_str(&joined);

    let mut hasher = Sha256::new();
    hasher.update(hash_input.as_bytes());
    let digest = hasher.finalize();
    let hex_str = hex::encode(digest);

    format!("{}#{}", parent_id, &hex_str[..16])
}

/// 見出し正規化（DD-LGX-003 §3 / §5.1、5 段階、v3 normalizer.rs 実測底本）。
/// ①装飾除去 ②NFC ③U+3000→ASCII空白 ④trim ⑤連続空白統合。
pub fn normalize_heading(text: &str) -> String {
    // 1. Markdown 装飾除去（長いマーカー優先: ** / __ / ~~ → * / _ / `）。
    //    単一 '_' / '*' の除去が新たな "~~" 等の二連マーカーを生成しうるため（例: "~_~" → "~~"）、
    //    不動点まで反復して冪等性を保つ（TS-LGX-003 ケース 13 の normalize 冪等性 property）。
    let mut s = text.to_string();
    loop {
        let before = s.clone();
        for marker in &["**", "__", "~~"] {
            s = s.replace(marker, "");
        }
        s = s.replace('*', "");
        s = s.replace('_', "");
        s = s.replace('`', "");
        if s == before {
            break;
        }
    }

    // 2. NFC 正規化
    let s: String = s.nfc().collect();

    // 3. 全角スペース U+3000 → ASCII スペース
    let s = s.replace('\u{3000}', " ");

    // 4. 前後トリム
    let s = s.trim();

    // 5. 連続 whitespace を単一 ASCII スペースへ統合
    let mut out = String::with_capacity(s.len());
    let mut prev_ws = false;
    for c in s.chars() {
        if c.is_whitespace() {
            if !prev_ws {
                out.push(' ');
            }
            prev_ws = true;
        } else {
            out.push(c);
            prev_ws = false;
        }
    }
    out
}

/// fragment が自動生成形式（ちょうど 16 文字小文字 hex `0-9a-f`）か判定
/// （DD-LGX-003 §3、SUBNODE-INV-6、v3 id_gen.rs:64-71 実測底本）。
pub fn is_auto_generated_fragment(fragment: &str) -> bool {
    if fragment.len() != 16 {
        return false;
    }
    fragment
        .chars()
        .all(|c| c.is_ascii_digit() || ('a'..='f').contains(&c))
}

/// 明示サブノード名（`#s:` 以降）の文字制約検証
/// （DD-LGX-003 §3、LGX-EXT-001 §4.5.2、v3 id_gen.rs:27-59 実測底本）。
/// 英数・ハイフン・アンダースコア、両端英数、1〜63 文字。
pub fn validate_explicit_name(name: &str) -> Result<(), String> {
    if name.is_empty() {
        return Err("explicit subnode name must not be empty".to_string());
    }
    if name.len() > 63 {
        return Err(format!(
            "explicit subnode name length {} exceeds 63",
            name.len()
        ));
    }
    for c in name.chars() {
        if !(c.is_ascii_alphanumeric() || c == '-' || c == '_') {
            return Err(format!(
                "explicit subnode name contains invalid character '{}'",
                c
            ));
        }
    }
    let first = match name.chars().next() {
        Some(c) => c,
        None => return Err("explicit subnode name must not be empty".to_string()),
    };
    let last = match name.chars().next_back() {
        Some(c) => c,
        None => return Err("explicit subnode name must not be empty".to_string()),
    };
    if !first.is_ascii_alphanumeric() {
        return Err(format!(
            "explicit subnode name must start with an alphanumeric character (got '{}')",
            first
        ));
    }
    if !last.is_ascii_alphanumeric() {
        return Err(format!(
            "explicit subnode name must end with an alphanumeric character (got '{}')",
            last
        ));
    }
    Ok(())
}

