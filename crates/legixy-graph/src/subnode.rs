/// 指定ノード ID の `#` fragment から `SubnodeKind` を分類する（DD-LGX-003 §2.2）。
/// `#s:` → Explicit、16 文字小文字 hex → AutoGenerated、`#` なし → Document。
/// SHARED-NEED: 統合時は Node.subnode_kind を直接参照する形へ置換する。
pub fn classify_subnode_kind(node_id: &str) -> SubnodeKind {
    match node_id.split_once('#') {
        None => SubnodeKind::Document,
        Some((_, fragment)) => {
            if fragment.starts_with("s:") {
                SubnodeKind::Explicit
            } else if is_auto_generated_fragment(fragment) {
                SubnodeKind::AutoGenerated
            } else {
                SubnodeKind::Explicit
            }
        }
    }
}

/// グラフ中の ParentChild エッジ（親→サブノード）を収集する補助
/// （TS-LGX-003 ケース 17 の ParentChild 生成検証用）。
/// SHARED-NEED: 統合時は TraceGraph 側 API（edges() の filter）に集約してよい。
pub fn parent_child_edges(graph: &TraceGraph) -> Vec<Edge> {
    graph
        .edges()
        .iter()
        .filter(|e| matches!(e.kind, crate::EdgeKind::ParentChild))
        .cloned()
        .collect()
}
